#for the first student
student1 = {}

student1["Name"] = input("Enter the first student name: ")
student1["maths"] = int(input("Enter the maths marks : "))
student1["phy"] = int(input("Enter the phy marks: "))
student1["bio"] = int(input("Enter the bio marks: "))

print(student1)



average = (student1["maths"] + student1["phy"] + student1["bio"] )/3

print(average)


if (student1["maths"]>student1["phy"] and student1["maths"]>student1["bio"]) :
    print("highest number is maths:", student1["maths"])

elif(student1["phy"]>student1["maths"] and student1["phy"]>student1["bio"]) :
    print("highest number is phy:", student1["phy"])  

elif(student1["bio"]>student1["maths"] and student1["bio"]>student1["phy"]) :
    print("highest number is bio:", student1["bio"]) 




if (student1["maths"]<student1["phy"] and student1["maths"]<student1["bio"]) :
    print("lowest number is maths:", student1["maths"])

elif(student1["phy"]<student1["maths"] and student1["phy"]<student1["bio"]) :
    print("lowest number is phy:", student1["phy"])  

elif(student1["bio"]<student1["maths"] and student1["bio"]<student1["phy"]) :
    print("lowest number is bio:", student1["bio"]) 

name = input("Enter the name :")
if student1["Name"] == name :
    del student1["Name"]
    print(student1)
else:
 print("Entered name is invalid")


maths = int(input("Enter the proper maths marks :"))
if student1["maths"] == maths:
    del student1["maths"]
    print(student1)
else:
 print("Entered marks is invalid")


